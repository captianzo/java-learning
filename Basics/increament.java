public class increament {
	public static void main(String[] args) {
		System.out.println("i++");
		int a = 5;
		System.out.println("a = " + a);
		++a;
		System.out.println("a = " + a);
		System.out.println("++i");
		int b = 6;
		System.out.println("b = " + b);
		System.out.println("b = " + (b++));
	}
}
